using UnityEngine;

public class UnitMultiSelectionUI : MonoBehaviour
{
    public UnitController UnitController { get; set; }
    public Selectable Selectable { get; set; }
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    void Start()
    {

    }

    // Update is called once per frame
    void Update()
    {

    }

    public void Select()
    {
        if (Selectable != null)
            SelectionManager.Instance.Select(Selectable);
    }

    public void OnMouseDown()
    {
        Select();
    }
}
